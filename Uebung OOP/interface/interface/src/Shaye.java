public class Shaye {
    private String name;
    private int gusheh;

    public Shaye(String name, int gusheh) {
        this.name = name;
        this.gusheh = gusheh;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getGusheh() {
        return gusheh;
    }

    public void setGusheh(int gusheh) {
        this.gusheh = gusheh;
    }


}
